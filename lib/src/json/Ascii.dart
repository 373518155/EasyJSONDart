  // 水平定位符号
  static const int CODE_HT = 9;
  static const String STR_HT = '\t';

  // 换行键
  static const int CODE_LF = 10;
  static const String STR_LF = '\n';

  // 换页键
  static const int CODE_FF = 12;
  static const String STR_FF = '\f';

  // 回车
  static const int CODE_CR = 13;
  static const String STR_CR = '\r';

  // 空格
  static const int CODE_SPACE = 32;
  static const String STR_SPACE = ' ';

  // 双引号
  static const int CODE_DOUBLE_QUOTES = 34;
  static const String STR_DOUBLE_QUOTES = '"';

  // 井号
  static const int CODE_HASH = 35;
  static const String STR_HASH = '#';

